/**
 * 진단 날짜 표시 공통 헬퍼.
 *
 * 백엔드 ISO8601 ("2026-02-26T05:29:20" / "2026-02-26T05:29:20+00:00") 를
 * 사용자 친화 형식 ("2026-02-26  05:29:20") 으로 변환한다.
 *
 * 노션 2번 피드백 A-3, C-5: 진단 이력 / 최근 진단 세션의 T 문자를
 * 스페이스 2칸으로 교체한다.
 */
export function formatSessionDate(value?: string | null): string {
  if (!value) return "-";
  const s = String(value);
  // "T" → "  " (스페이스 2칸) — 단순 치환. 타임존 표기는 제거.
  // "2026-02-26T05:29:20" → "2026-02-26  05:29:20"
  // "2026-02-26T05:29:20+00:00" → "2026-02-26  05:29:20"
  const noTz = s.replace(/([+-]\d{2}:?\d{2}|Z)$/, "");
  return noTz.replace("T", "  ");
}
