def main() -> int:
    if not XLSX_PATH.exists():
        print(f"[ERR] xlsx not found: {XLSX_PATH}", file=sys.stderr)
        return 1

    wb = openpyxl.load_workbook(XLSX_PATH)
    if SHEET not in wb.sheetnames:
        print(f"[ERR] sheet '{SHEET}' missing", file=sys.stderr)
        return 1
    ws = wb[SHEET]

    # 항목ID(col1) → row 번호
    iid_to_row: dict[str, int] = {}
    for r in range(4, ws.max_row + 1):
        iid = ws.cell(row=r, column=1).value
        if iid:
            iid_to_row[str(iid).strip()] = r

    targets = list(RECOMMENDATIONS.keys())
    print(f"[info] targets={len(targets)}  xlsx_rows={len(iid_to_row)}")

    updated, skipped_present, missing = 0, 0, 0
    char_u, char_p, char_s = [], [], []

    for iid in targets:
        if iid not in iid_to_row:
            print(f"[WARN] item_id not found in xlsx: {iid}")
            missing += 1
            continue
        r = iid_to_row[iid]
        recs = RECOMMENDATIONS[iid]

        # idempotent: 비어있는 셀만 채움
        c9 = ws.cell(row=r, column=9)
        c10 = ws.cell(row=r, column=10)
        c11 = ws.cell(row=r, column=11)
        c12 = ws.cell(row=r, column=12)

        wrote_any = False
        if not (c9.value and str(c9.value).strip()):
            if "u" in recs:
                c9.value = recs["u"]
                char_u.append(len(recs["u"]))
                wrote_any = True
        else:
            char_u.append(len(str(c9.value)))

        if not (c10.value and str(c10.value).strip()):
            if "p" in recs:
                c10.value = recs["p"]
                char_p.append(len(recs["p"]))
                wrote_any = True
        else:
            char_p.append(len(str(c10.value)))

        if not (c11.value and str(c11.value).strip()):
            if "s" in recs:
                c11.value = recs["s"]
                char_s.append(len(recs["s"]))
                wrote_any = True
        else:
            char_s.append(len(str(c11.value)))

        # col12 갱신
        if c9.value or c10.value or c11.value:
            c12.value = "있음"

        if wrote_any:
            updated += 1
            # 행별 글자수 검증 로그
            u_len = len(str(c9.value)) if c9.value else 0
            p_len = len(str(c10.value)) if c10.value else 0
            s_len = len(str(c11.value)) if c11.value else 0
            print(f"  {iid:14s} u={u_len:4d}  p={p_len:4d}  s={s_len:4d}")
        else:
            skipped_present += 1

    wb.save(XLSX_PATH)

    # 통계
    print()
    print("=== summary ===")
    print(f"updated_rows       = {updated}")
    print(f"skipped_already_set= {skipped_present}")
    print(f"missing_in_xlsx    = {missing}")
    print()
    print("=== 글자수 통계 (작성 + 기존 포함, 33행 기준) ===")

    def _stats(name: str, arr: list[int], threshold: int) -> None:
        if not arr:
            print(f"  {name}: (empty)")
            return
        mean = statistics.mean(arr)
        flag = "OK" if mean >= threshold else "LOW"
        print(
            f"  {name:18s} n={len(arr):3d}  min={min(arr):4d}  max={max(arr):4d}  "
            f"mean={mean:6.1f}  threshold={threshold}  [{flag}]"
        )

    _stats("미충족(col9)", char_u, 100)
    _stats("부분충족(col10)", char_p, 100)
    _stats("관련솔루션(col11)", char_s, 30)
    return 0


if __name__ == "__main__":
    sys.exit(main())
